public class PrimitiveData {
static int a;
static long b;
static double c;
static float d;
static String e;
static boolean f;
public static void main(String[] args) {
	System.out.println("integer value =" + a);
	System.out.println("long value =" + b);
	System.out.println("double value =" + c);
	System.out.println("float value =" + d);
	System.out.println("String value =" + e);
	System.out.println("Boolean valuse =" + f);
	}
}
